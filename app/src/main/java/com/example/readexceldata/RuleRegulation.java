package com.example.readexceldata;

public class RuleRegulation {
    String ordinance;
    String title_no;
    String title;
    String url;
    String version;
    String num;
    String content;

    public String getOrdinance() {
        return ordinance;
    }

    public void setOrdinance(String ordinance) {
        this.ordinance = ordinance;
    }

    public String getTitle_no() {
        return title_no;
    }

    public void setTitle_no(String title_no) {
        this.title_no = title_no;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    public String getNum() {
        return num;
    }

    public void setNum(String num) {
        this.num = num;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }
}